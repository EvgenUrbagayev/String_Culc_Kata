public class Arithmetic {
    public static void main(String[] args) {
        int a=5, b=3;
        int c = a+b;
        System.out.println(c);
        short d = (short) (c+5+a+3);
        System.out.println(d);

        float n = 5.8f, m = 7.6f;
        double v = n - m;
        System.out.println(v);

    }
}
